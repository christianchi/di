package com.dam.di.customlistview.activities;

public class ItemFruta {
	
	String texto;
	int imagen;
	
	public ItemFruta(String texto, int imagen) {
		this.texto = texto;
		this.imagen = imagen;
	}
	public String getTexto() {
		return texto;
	}
	public void setTexto(String texto) {
		this.texto = texto;
	}
	public int getImagen() {
		return imagen;
	}
	public void setImagen(int imagen) {
		this.imagen = imagen;
	}
	
	

}
