package com.example.menucontextuallist;

public class ItemFruta {
	
	String texto;
	String imagen;
	
	public ItemFruta(String texto, String imagen) {
		this.texto = texto;
		this.imagen = imagen;
	}
	public String getTexto() {
		return texto;
	}
	public void setTexto(String texto) {
		this.texto = texto;
	}
	public String getImagen() {
		return imagen;
	}
	public void setImagen(String imagen) {
		this.imagen = imagen;
	}
	
	

}
